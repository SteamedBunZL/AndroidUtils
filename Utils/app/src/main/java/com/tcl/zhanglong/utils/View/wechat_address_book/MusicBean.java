public class MusicBean {

    private String tag;
    private String music;

    public MusicBean(String music) {
        this.music = music;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getMusic() {
        return music;
    }

    public void setMusic(String music) {
        this.music = music;
    }
}
